THE
JUBILEE.

8-11. thou shalt number seven sabbaths of years--This most
extraordinary of all civil institutions, which received the name of
"Jubilee" from a Hebrew word signifying a musical instrument, a
horn or trumpet, began on the tenth day of the seventh month, or the
great day of atonement, when, by order of the public authorities, the
sound of trumpets proclaimed the beginning of the universal redemption.
All prisoners and captives obtained their liberties, slaves were
declared free, and debtors were absolved. The land, as on the sabbatic
year, was neither sowed nor reaped, but allowed to enjoy with its
inhabitants a sabbath of repose; and its natural produce was the common
property of all. Moreover, every inheritance throughout the land of
Judea was restored to its original owner.

