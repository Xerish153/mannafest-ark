On this concluding section of the Epistle, Note, (1) In the minute
and delicate manifestations of Christian feeling, and lively interest in
the smallest movements of Christian life, love, and zeal, which are here
exemplified, combined with the grasp of thought and elevation of soul
which this whole Epistle displays, as indeed all the writings of our
apostle, we have the secret of much of that grandeur of character which
has made the name of Paul stand on an elevation of its own in the
estimation of enlightened Christendom in every age, and of that
influence which under God, beyond all the other apostles, he has already
exercised, and is yet destined to exert, over the religious thinking and
feeling of men. Nor can any approach him in these peculiarities without
exercising corresponding influence on all with whom they come in contact

(Ro 16:1-16).

(2) "The wisdom of the serpent and the harmlessness of the dove"--in
enjoining which our apostle here only echoes the teaching of his Lord

(Mt 10:16)

--is a combination of properties the rarity of which among Christians
is only equalled by its vast importance. In every age of the Church
there have been real Christians whose excessive study of the serpent''s
wisdom has so sadly trenched upon their guileless simplicity, as at
times to excite the distressing apprehension that they were no better
than wolves in sheep''s clothing. Nor is it to be denied, on the other
hand, that, either from inaptitude or indisposition to judge with manly
discrimination of character and of measures, many eminently simple,
spiritual, devoted Christians, have throughout life exercised little or
no influence on any section of society around them. Let the apostle''s
counsel on this head

(Ro 16:19)

be taken as a study, especially by young Christians, whose character
has yet to be formed, and whose permanent sphere in life is but
partially fixed; and let them prayerfully set themselves to the
combined exercise of both those qualities. So will their Christian
character acquire solidity and elevation, and their influence for good
be proportionably extended. (3) Christians should cheer their own and
each other''s hearts, amidst the toils and trials of their protracted
warfare, with the assurance that it will have a speedy and glorious
end; they should accustom themselves to regard all opposition to the
progress and prosperity of Christ''s cause--whether in their own souls,
in the churches with which they are connected, or in the world at
large--as just "Satan" in conflict, as ever, with Christ their Lord;
and they should never allow themselves to doubt that "the God of peace"
will "shortly" give them the neck of their Enemy, and make them to
bruise the Serpent''s head

(Ro 16:20).

(4) As Christians are held up and carried through solely by divine
power, working through the glorious Gospel, so to that power, and to
the wisdom that brought that Gospel nigh to them, they should ascribe
all the glory of their stability now, as they certainly will of their
victory at last

(Ro 16:25-27).

(5) "Has the everlasting God . . . commanded" that the Gospel
"mystery," so long kept hid but now fully disclosed, shall be "made
known to all nations for the obedience of faith"

(Ro 16:26)?

Then, what "necessity is laid upon" all the churches and every
Christian, to send the Gospel "to every creature!" And we may rest well
assured that the prosperity or decline of churches, and of individual
Christians, will have not a little to do with their faithfulness or
indifference to this imperative duty.

