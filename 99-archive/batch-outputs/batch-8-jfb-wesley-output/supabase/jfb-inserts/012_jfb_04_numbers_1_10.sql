2-5. Let the children of Israel also keep the passover at his
appointed season, &c.--The date of this command to keep the passover
in the wilderness was given shortly after the erection and consecration
of the tabernacle and preceded the numbering of the people by a month.
(Compare

Nu 9:1
with Nu 1:1, 2).

But it is narrated after that transaction in order to introduce the
notice of a particular case, for which a law was provided to meet the
occasion. This was the first observance of the passover since the
exodus; and without a positive injunction, the Israelites were under no
obligation to keep it till their settlement in the land of Canaan

(Ex 12:25).

The anniversary was kept on the exact day of the year on which they,
twelve months before, had departed from Egypt; and it was marked by all
the peculiar rites--the he lamb and the unleavened bread. The materials
would be easily procured--the lambs from their numerous flocks and the
meal for the unleavened bread, by the aid of Jethro, from the land of
Midian, which was adjoining their camp

(Ex 3:1).

But their girded loins, their sandaled feet, and their staff in their
hand, being mere circumstances attending a hurried departure and not
essential to the rite, were not repeated. It is supposed to have been
the only observance of the feast during their forty years'' wandering;
and Jewish writers say that, as none could eat the passover except they
were circumcised

(Ex 12:43, 44, 48),

and circumcision was not practised in the wilderness

[Jos 5:4-7],

there could be no renewal of the paschal solemnity.

