2. Make thee two trumpets of silver--These trumpets were of a long
form, in opposition to that of the Egyptian trumpets, with which the
people were convened to the worship of Osiris and which were curved
like rams'' horns. Those which Moses made, as described by
JOSEPHUS and
represented on the arch of Titus, were straight, a cubit or more in
length, the tubes of the thickness of a flute. Both extremities bore a
close resemblance to those in use among us. They were of solid
silver--so as, from the purity of the metal, to give a shrill, distinct
sound; and there were two of them, probably because there were only two
sons of Aaron; but at a later period the number was greatly increased

(Jos 6:8;
2Ch 5:12).

And although the camp comprehended 2,500,000 of people, two trumpets
would be quite sufficient, for sound is conveyed easily through the
pure atmosphere and reverberated strongly among the valleys of the
Sinaitic hills.

3-7. when they shall blow with them--There seem to have been signals
made by a difference in the loudness and variety in the notes, suited
for different occasions, and which the Israelites learned to
distinguish. A simple uniform sound by both trumpets summoned a general
assembly of the people; the blast of a single trumpet convoked the
princes to consult on public affairs; notes of some other kind were
made to sound an alarm, whether for journeying or for war. One alarm
was the recognized signal for the eastern division of the camp (the
tribes of Judah, Issachar, and Zebulun) to march; two alarms gave the
signal for the southern to move; and, though it is not in our present
Hebrew text, the Septuagint has, that on three alarms being
sounded, those on the west; while on four blasts, those on the north
decamped. Thus the greatest order and discipline were established in
the Israelitish camp--no military march could be better regulated.

8. the sons of Aaron the priests shall blow with the trumpets,
&c.--Neither the Levites nor any in the common ranks of the people
could be employed in this office of signal giving. In order to attract
greater attention and more faithful observance, it was reserved to the
priests alone, as the Lord''s ministers; and as anciently in Persia and
other Eastern countries the alarm trumpets were sounded from the tent of
the sovereign, so were they blown from the tabernacle, the visible
residence of Israel''s King.

9. If ye go to war--In the land of Canaan, either when attacked by
foreign invaders or when they went to take possession according to the
divine promise, "ye [that is, the priests] shall blow an alarm." This
advice was accordingly acted upon

(Nu 31:6;
2Ch 13:12);

and in the circumstances it was an act of devout confidence in God. A
solemn and religious act on the eve of a battle has often animated the
hearts of those who felt they were engaged in a good and just cause;
and so the blowing of the trumpet, being an ordinance of God, produced
that effect on the minds of the Israelites. But more is meant by the
words--namely, that God would, as it were, be aroused by the trumpet to
bless with His presence and aid.

10. Also in the day of your gladness, and in your solemn days--Festive
and thanksgiving occasions were to be ushered in with the trumpets, as
all feasts afterwards were

(Ps 81:3;
2Ch 29:27)

to intimate the joyous and delighted feelings with which they engaged
in the service of God.

11. It came to pass on the twentieth day of the second month, in the
second year, &c.--The Israelites had lain encamped in Wady-Er-Rahah
and the neighboring valleys of the Sinaitic range for the space of
eleven months and twenty-nine days. (Compare

Ex 19:1).

Besides the religious purposes of the highest importance to which their
long sojourn at Sinai was subservient, the Israelites, after the
hardships and oppression of the Egyptian servitude, required an
interval of repose and refreshment. They were neither physically nor
morally in a condition to enter the lists with the warlike people they
had to encounter before obtaining possession of Canaan. But the
wondrous transactions at Sinai--the arm of Jehovah so visibly displayed
in their favor--the covenant entered into, and the special blessings
guaranteed, beginning a course of moral and religious education which
moulded the character of this people--made them acquainted with their
high destiny and inspired them with those noble principles of divine
truth and righteousness which alone make a great nation.

12. wilderness of Paran--It stretched from the base of the Sinaitic
group, or from Et-Tyh, over that extensive plateau to the southwestern
borders of Palestine.

13-27. the children of Israel took their journey . . . by the hand of
Moses--It is probable that Moses, on the breaking up of the encampment,
stationed himself on some eminence to see the ranks defile in order
through the embouchure of the mountains. The marching order is
described

(Nu 2:1-34);

but, as the vast horde is represented here in actual migration, let us
notice the extraordinary care that was taken for ensuring the safe
conveyance of the holy things. In the rear of Judah, which, with the
tribes of Issachar and Zebulun, led the van, followed the Gershonites
and Merarites with the heavy and coarser materials of the tabernacle.
Next in order were set in motion the flank divisions of Reuben and
Ephraim. Then came the Kohathites, who occupied the center of the
moving mass, bearing the sacred utensils on their shoulder. They were
so far behind the other portions of the Levitical body that these would
have time at the new encampment to rear the framework of the tabernacle
before the Kohathites arrived. Last of all, Dan, with the associated
tribes, brought up the rear of the immense caravan. Each tribe was
marshalled under its prince or chief and in all their movements rallied
around its own standard.

