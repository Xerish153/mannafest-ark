2. Command the children of Israel, that they put out of the camp
every leper--The exclusion of leprous persons from the camp in the
wilderness, as from cities and villages afterwards, was a sanitary
measure taken according to prescribed rules

(Le 13:1-14:57).

This exclusion of lepers from society has been acted upon ever since;
and it affords almost the only instance in which any kind of attention
is paid in the East to the prevention of contagion. The usage still
more or less prevails in the East among people who do not think the
least precaution against the plague or cholera necessary; but judging
from personal observation, we think that in Asia the leprosy has now
much abated in frequency and virulence. It usually appears in a
comparatively mild form in Egypt, Palestine, and other countries where
the disorder is, or was, endemic. Small societies of excluded lepers
live miserably in paltry huts. Many of them are beggars, going out into
the roads to solicit alms, which they receive in a wooden bowl;
charitable people also sometimes bring different articles of food,
which they leave on the ground at a short distance from the hut of the
lepers, for whom it is intended. They are generally obliged to wear a
distinctive badge that people may know them at first sight and be
warned to avoid them. Other means were adopted among the ancient Jews
by putting their hand on their mouth and crying, "Unclean, unclean"

[Le 13:45].

But their general treatment, as to exclusion from society, was the same
as now described. The association of the lepers, however, in this
passage, with those who were subject only to ceremonial uncleanness,
shows that one important design in the temporary exile of such persons
was to remove all impurities that reflected dishonor on the character
and residence of Israel''s King. And this vigilant care to maintain
external cleanliness in the people was typically designed to teach them
the practice of moral purity, or cleansing themselves from all
filthiness of the flesh and spirit. The regulations made for ensuring
cleanliness in the camp suggest the adoption of similar means for
maintaining purity in the church. And although, in large communities of
Christians, it may be often difficult or delicate to do this, the
suspension or, in flagrant cases of sin, the total excommunication of
the offender from the privileges and communion of the church is an
imperative duty, as necessary to the moral purity of the Christian as
the exclusion of the leper from the camp was to physical health and
ceremonial purity in the Jewish church.

