10-13. David''s heart smote him after that he had numbered the people.
And David said unto the Lord, I have sinned--The act of numbering the
people was not in itself sinful; for Moses did it by the express
authority of God. But David acted not only independently of such order
or sanction, but from motives unworthy of the delegated king of Israel;
from pride and vainglory; from self-confidence and distrust of God;
and, above all, from ambitious designs of conquest, in furtherance of
which he was determined to force the people into military service, and
to ascertain whether he could muster an army sufficient for the
magnitude of the enterprises he contemplated. It was a breach of the
constitution, an infringement of the liberties of the people, and
opposed to that divine policy which required that Israel should
continue a separate people. His eyes were not opened to the heinousness
of his sin till God had spoken unto him by His commissioned prophet.

