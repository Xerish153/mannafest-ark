according to the subsidence of the stream. The water is distributed in
small channels or earthen conduits, simple in construction, worked by
the foot, and formed with a mattock by the gardener who directs their
course, and which are banked up or opened, as occasion may require, by
pressing in the soil with the foot. Thus was the land watered in which
the Israelites had dwelt so long. Such vigilance and laborious industry
would not be needed in the promised land. Instead of being visited with
moisture only at one brief season and left during the rest of the year
under a withering blight, every season it would enjoy the benign
influences of a genial climate. The hills would attract the frequent
clouds, and in the refreshing showers the blessing of God would
especially rest upon the land.

