Palestine present a striking contrast to those of the land of bondage.
A widely extending plain forms the cultivated portion of Egypt, and on
the greater part of this low and level country rain never falls. This
natural want is supplied by the annual overflow of the Nile, and by
artificial means from the same source when the river has receded within
its customary channel. Close by the bank the process of irrigation is
very simple. The cultivator opens a small sluice on the edge of the
square bed in which seed has been sown, making drill after drill; and
when a sufficient quantity of water has poured in, he shuts it up with
his foot. Where the bank is high, the water is drawn up by hydraulic
