REMAINDER OF THE
LAND
DESCRIBED.

2. there remained . . . seven tribes, which had not yet received their
inheritance--The selection of Shiloh for the seat of worship, together
with the consequent removal of the camp thither, had necessarily
interrupted the casting of lots, which was commenced by fixing
localities for the tribes of Judah and Joseph. Various causes led to a
long delay in resuming it. The satisfaction of the people with their
change to so pleasant and fertile a district, their preference of a
nomad life, a love of ease, and reluctance to renew the war, seem to
have made them indifferent to the possession of a settled inheritance.
But Joshua was too much alive to the duty laid on him by the Lord to
let matters continue in that state; and accordingly, since a general
conquest of the land had been made, he resolved to proceed immediately
with the lot, believing that when each tribe should receive its
inheritance, a new motive would arise to lead them to exert themselves
in securing the full possession.

3. How long are ye slack to go to possess the land, which the Lord God
of your fathers hath given you--This reproof conveys an impression that
the seven tribes were dilatory to a criminal extent.

4-9. Give out from among you three men for each tribe--Though the lot
determined the part of the country where each tribe was to be located,
it could not determine the extent of territory which might be required;
and the dissatisfaction of the children of Joseph with the alleged
smallness of their possession gave reason to fear that complaints might
arise from other quarters, unless precautions were taken to make a
proper distribution of the land. For this purpose a commission was
given to twenty-one persons--three chosen from each of the seven tribes
which had not yet received their inheritance, to make an accurate
survey of the country.

9. The men went and passed through the land, and described it by cities
into seven parts in a book--dividing the land according to
its value, and the worth of the cities which it contained, into seven
equal portions. This was no light task to undertake. It required
learning and intelligence which they or their instructors had, in all
probability, brought with them out of Egypt. Accordingly,
JOSEPHUS says
that the survey was performed by men expert in geometry. And, in fact,
the circumstantial account which is given of the boundaries of each
tribe and its situation, well proves it to have been the work of no
mean or incompetent hands.

