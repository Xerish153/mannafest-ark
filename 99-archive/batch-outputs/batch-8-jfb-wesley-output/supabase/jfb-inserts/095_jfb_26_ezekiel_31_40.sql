(1Ki 6:3)

"twenty . . . ten cubits." The breadth perhaps was ten
and a half;
1Ki 6:3

designates the number by the lesser next round number, "ten";
Ezekiel here, by the larger number, "eleven" [MENOCHIUS]. The Septuagint reads "twelve."

