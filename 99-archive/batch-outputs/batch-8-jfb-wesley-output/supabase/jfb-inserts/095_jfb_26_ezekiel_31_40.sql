different from those subsisting before the captivity. There are things
in it so improbable physically as to preclude a purely literal
interpretation. The general truth seems to hold good that, as Israel
served the nations for his rejection of Messiah, so shall they serve him
in the person of Messiah, when he shall acknowledge Messiah

(Isa 60:12;
Zec 14:17-19;

compare

Ps 72:11).

The ideal temple exhibits, under Old Testament forms (used as being
those then familiar to the men whom Ezekiel, a priest himself, and one
who delighted in sacrificial images, addresses), not the precise
literal outline, but the essential character of the worship of
Messiah as it shall be when He shall exercise sway in Jerusalem among
His own people, the Jews, and thence to the ends of the earth. The very
fact that the whole is a vision

(Eze 40:2),

not an oral face-to-face communication such as that granted to Moses

(Nu 12:6-8),

implies that the directions are not to be understood so precisely
literally as those given to the Jewish lawgiver. The description
involves things which, taken literally, almost involve natural
impossibilities. The square of the temple, in

Eze 42:20,

is six times as large as the circuit of the wall enclosing the old
temple, and larger than all the earthly Jerusalem. Ezekiel gives three
and a half miles and one hundred forty yards to his temple square. The
boundaries of the ancient city were about two and a half miles. Again,
the city in Ezekiel has an area between three or four thousand square
miles, including the holy ground set apart for the prince, priests, and
Levites. This is nearly as large as the whole of Judea west of the
Jordan. As Zion lay in the center of the ideal city, the one-half of
the sacred portion extended to nearly thirty miles south of Jerusalem,
that is, covered nearly the whole southern territory, which reached
only to the Dead Sea

(Eze 47:19),

and yet five tribes were to have their inheritance on that side of
Jerusalem, beyond the sacred portion

(Eze 48:23-28).

Where was land to be found for them there? A breadth of but four or
five miles apiece would be left. As the boundaries of the land are
given the same as under Moses, these incongruities cannot be explained
away by supposing physical changes about to be effected in the land
such as will meet the difficulties of the purely literal
interpretation. The distribution of the land is in equal portions among
the twelve tribes, without respect to their relative numbers, and the
parallel sections running from east to west. There is a difficulty
also in the supposed separate existence of the twelve tribes, such
separate tribeships no longer existing, and it being hard to imagine
how they could be restored as distinct tribes, mingled as they now are.
So the stream that issued from the east threshold of the temple and
flowed into the Dead Sea, in the rapidity of its increase and the
quality of its waters, is unlike anything ever known in Judea or
elsewhere in the world. Lastly, the catholicity of the Christian
dispensation, and the spirituality of its worship, seem incompatible
with a return to the local narrowness and "beggarly elements" of the
Jewish ritual and carnal ordinances, disannulled "because of the
unprofitableness thereof" [FAIRBAIRN],

(Ga 4:3, 9; 5:1;
Heb 9:10; 10:18).

"A temple with sacrifices now would be a denial of the all-sufficiency
of the sacrifice of Christ. He who sacrificed before confessed the
Messiah. He who should sacrifice now would solemnly deny Him" [DOUGLAS]. These difficulties, however, may be all
seeming, not real. Faith accepts God''s Word as it is, waits for
the event, sure that it will clear up all such difficulties. Perhaps,
as some think, the beau ideal of a sacred commonwealth is given
according to the then existing pattern of temple services, which would
be the imagery most familiar to the prophet and his hearers at the
time. The minute particularizing of details is in accordance with
Ezekiel''s style, even in describing purely ideal scenes. The old temple
embodied in visible forms and rites spiritual truths affecting the
people even when absent from it. So this ideal temple is made in the
absence of the outward temple to serve by description the same purpose
of symbolical instruction as the old literal temple did by forms and
acts. As in the beginning God promised to be a "sanctuary"

(Eze 11:16)

to the captives at the Chebar, so now at the close is promised a
complete restoration and realization of the theocratic worship and
polity under Messiah in its noblest ideal (compare

Jer 31:38-40).

In

Re 21:22

"no temple" is seen, as in the perfection of the new dispensation the
accidents of place and form are no longer needed to realize to
Christians what Ezekiel imparts to Jewish minds by the imagery familiar
to them. In Ezekiel''s temple holiness stretches over the entire temple,
so that in this there is no longer a distinction between the different
parts, as in the old temple: parts left undeterminate in the latter
obtain now a divine sanction, so that all arbitrariness is excluded. So
that it is be a perfect manifestation of the love of God to His
covenant-people

(Eze 40:1-43:12);

and from it, as from a new center of religious life, there gushes forth
the fulness of blessings to them, and so to all people

(Eze 47:1-23)

[FAIRBAIRN and HAVERNICK].
The temple built at the return from Babylon can only very partially
have realized the model here given. The law is seemingly opposed to the
gospel

(Mt 5:21, 22, 27, 28, 33, 34).

It is not really so (compare

Mt 5:17, 18;
Ro 3:31;
Ga 3:21, 22).

It is true Christ''s sacrifice superseded the law sacrifices

(Heb 10:12-18).

Israel''s province may hereafter be to show the essential identity, even
in the minute details of the temple sacrifices, between the law and
gospel

(Ro 10:8).

The ideal of the theocratic temple will then first be realized.

1. beginning of the year--the ecclesiastical year, the first month
of which was Nisan.

the city . . . thither--Jerusalem, the center to which all the
prophet''s thoughts tended.

2. visions of God--divinely sent visions.

very high mountain--Moriah, very high, as compared with the plains
of Babylon, still more so as to its moral elevation

(Eze 17:22; 20:40).

by which--Ezekiel coming from the north is set down at (as the
Hebrew for "upon" may be translated) Mount Moriah, and sees the
city-like frame of the temple stretching southward. In

Eze 40:3,

"God brings him thither," that is, close up to it, so as to inspect it
minutely (compare

Re 21:10).

In this closing vision, as in the opening one of the book, the divine
hand is laid on the prophet, and he is borne away in the visions of
God. But the scene there was by the Chebar, Jehovah having forsaken
Jerusalem; now it is the mountain of God, Jehovah having returned
thither; there, the vision was calculated to inspire terror; here, hope
and assurance.

3. man--The Old Testament manifestations of heavenly beings as men prepared men''s minds for the coming incarnation.

brass--resplendent.

line--used for longer measurements

(Zec 2:1).

reed--used in measuring houses

(Re 21:15).

It marked the straightness of the walls.

5. Measures were mostly taken from the human body. The
greater cubit, the length from the elbow to the end of the
middle finger, a little more than two feet: exceeding the ordinary
cubit (from the elbow to the wrist) by an hand-breadth, that is,
twenty-one inches in all. Compare

Eze 43:13,
with Eze 40:5.

The palm was the full breadth of the hand, three and a half
inches.

breadth of the building--that is, the boundary wall. The imperfections
in the old temple''s boundary wall were to have no place here. The
buildings attached to it had been sometimes turned to common uses; for
example, Jeremiah was imprisoned in one

(Jer 20:2; 29:26).

But now all these were to be holy to the Lord. The gates and doorways
to the city of God were to be imprinted in their architecture with the
idea of the exclusion of everything defiled

(Re 21:27).

The east gate was to be especially sacred, as it was through it the
glory of God had departed

(Eze 11:23),

and through it the glory was to return

(Eze 43:1, 2; 44:2, 3).

6. the stairs--seven in number

(Eze 40:26).

threshold--the sill [FAIRBAIRN].

other threshold--FAIRBAIRN considers there
is but one threshold, and translates, "even the one threshold, one rod
broad." But there is another threshold mentioned in

Eze 40:7.

The two thresholds here seem to be the upper and the lower.

7. chamber--These chambers were for the use of the Levites who watched
at the temple gates; guard-chambers

(2Ki 22:4;
1Ch 9:26, 27);

also used for storing utensils and musical instruments.

9. posts--projecting column-faced fronts of the sides of the doorway,
opposite to one another.

12. space--rather, "the boundary."

16. narrow--latticed [HENDERSON]. The ancients had no glass, so they
had them latticed, narrow in the interior of the walls, and widening at
the exterior. "Made fast," or "firmly fixed in the chambers"
[MAURER].

arches--rather, "porches."

17. pavement--tesselated mosaic

(Es 1:6).

chambers--serving as lodgings for the priests on duty in the temple,
and as receptacles of the tithes of salt, wine, and oil.

18. The higher pavement was level with the entrance of the gates, the
lower was on either side of the raised pavement thus formed. Whereas
Solomon''s temple had an outer court open to alterations and even
idolatrous innovations

(2Ki 23:11, 12;
1Ch 20:5),

in this there was to be no room for human corruptions. Its compass was
exactly defined, one hundred cubits; and the fine pavement implied it
was to be trodden only by clean feet (compare

Isa 35:8).

20-27. The different approaches corresponded in plan. In the case of
these two other gates, however, no mention is made of a building with
thirty chambers such as was found on the east side. Only one was needed,
and it was assigned to the east as being the sacred quarter, and that
most conveniently situated for the officiating priests.

23. and toward the east--an elliptical expression for "The gate of the
inner court was over against the (outer) gate toward the north
(just as the inner gate was over against the outer gate) toward the east."

28-37. The inner court and its gates.

according to these measures--namely, the measures of the outer gate.
The figure and proportions of the inner answered to the outer.

30. This verse is omitted in the Septuagint, the Vatican
manuscript, and others. The dimensions here of the inner gate do not
correspond to the outer, though

Eze 40:28

asserts that they do. HAVERNICK, retaining the
verse, understands it of another porch looking inwards toward the
temple.

arches--the porch [FAIRBAIRN]; the columns on which the arches rest
[HENDERSON].

31. eight steps--The outer porch had only seven

(Eze 40:26).

37. posts--the Septuagint and Vulgate read, "the
porch," which answers better to

Eze 40:31-34.

"The arches" or "porch" [MAURER].

38. chambers . . . entries--literally, "a chamber and
its door."

by the posts--that is, at or close by the posts or
columns.

where they washed the burnt offering--This does not apply to all the
gates but only to the north gate. For

Le 1:11

directs the sacrifices to be killed north of the altar; and

Eze 8:5

calls the north gate, "the gate of the altar." And

Eze 40:40

particularly mentions the north gate.

43. hooks--cooking apparatus for cooking the flesh of the sacrifices
that fell to the priests. The hooks were "fastened" in the walls within
the apartment, to hang the meat from, so as to roast it. The Hebrew
comes from a root "fixed" or "placed."

44. the chambers of the singers--two in number, as proved by what
follows: "and their prospect (that is, the prospect of one) was toward
the south, (and) one toward the north." So the Septuagint.

46. Zadok--lineally descended from Aaron. He had the high priesthood
conferred on him by Solomon, who had set aside the family of Ithamar
because of the part which Abiathar had taken in the rebellion of
Adonijah

(1Ki 1:7; 2:26, 27).

47. court, an hundred cubits . . . foursquare--not to be confounded
with the inner court, or court of Israel, which was open to all who had
sacrifices to bring, and went round the three sides of the sacred
territory, one hundred cubits broad. This court was one hundred cubits
square, and had the altar in it, in front of the temple. It was the
court of the priests, and hence is connected with those who had charge
of the altar and the music. The description here is brief, as the things
connected with this portion were from the first divinely regulated.

48, 49. These two verses belong to the forty-first chapter, which
treats of the temple itself.

