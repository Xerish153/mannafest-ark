ROLL.
The fraudulent and perjuring transgressors of the law shall be
extirpated from Judea.

1. flying roll--of papyrus, or dressed skins, used for writing on
when paper was not known. It was inscribed with the words of the curse

(De 27:15-26; 28:15-68).

Being written implied that its contents were beyond all escape or
repeal

(Eze 2:9).

Its "flying" shows that its curses were ready swiftly to visit the
transgressors. It was unrolled, or else its dimensions could not have
been seen

(Zec 5:2).

Being open to all, none could say in excuse he knew not the law and the
curses of disobedience. As the previous visions intimated God''s favor in
restoring the Jewish state, so this vision announces judgment,
intimating that God, notwithstanding His favor, did not approve of their
sins. Being written on both sides, "on this and on that side"

(Zec 5:3)

[VATABLUS] connects it with the two tables of the law

(Ex 32:15),

and implies its comprehensiveness. One side denounced "him that
sweareth falsely

(Zec 5:4)

by God''s name," according to the third commandment of the first
table, duty to God; the other side denounced theft,
according to the eighth commandment, which is in the second
table, duty to one''s neighbor.

2. length . . . twenty cubits . . . breadth
. . . ten cubits--thirty feet by fifteen, the dimensions
of the temple porch

(1Ki 6:3),

where the law was usually read, showing that it was divinely
authoritative in the theocracy. Its large size implies the great number
of the curses contained. The Hebrew for "roll" or "volume" is
used of the law

(Ps 40:7).

3. curse . . . earth--

(Mal 4:6).

The Gentiles are amenable to the curse of the law, as they have its
substance, so far as they have not seared and corrupted conscience,
written on their hearts

(Ro 2:15).

cut off--literally, "cleared away."

