30. I and my Father are one--Our language admits not of the
precision of the original in this great saying. "Are" is in the
masculine gender--"we (two persons) are"; while "one" is
neuter--"one thing." Perhaps "one interest"
expresses, as nearly as may be, the purport of the saying. There seemed
to be some contradiction between His saying they had been given by His
Father into His own hands, out of which they could not be
plucked, and then saying that none could pluck them out of His
Father''s hands, as if they had not been given out of them.
"Neither have they," says He; "though He has given them to Me,
they are as much in His own almighty hands as ever--they cannot
be, and when given to Me they are not, given away from Himself; for
HE AND I HAVE ALL IN COMMON."
Thus it will be seen, that, though oneness of essence is not the
precise thing here affirmed, that truth is the basis of what is
affirmed, without which it would not be true. And AUGUSTINE was right in saying the "We are"
condemns the Sabellians (who denied the distinction of
Persons in the Godhead), while the "one" (as explained)
condemns the Arians (who denied the unity of their essence).

