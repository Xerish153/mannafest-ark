1. Mount Moriah, where the Lord appeared unto David--These words
seem to intimate that the region where the temple was built was
previously known by the name of Moriah

(Ge 22:2),

and do not afford sufficient evidence for affirming, as has been done
[STANLEY], that the name was first given to
the mount, in consequence of the vision seen by David. Mount
Moriah was one summit of a range of hills which went under the general
name of Zion. The platform of the temple is now, and has long been,
occupied by the haram, or sacred enclosure, within which stand the
three mosques of Omar (the smallest), of El Aksa, which in early times
was a Christian church, and of Kubbet el Sakhara, "The dome of the
rock," so called from a huge block of limestone rock in the center of
the floor, which, it is supposed, formed the elevated threshing-floor
of Araunah, and on which the great brazen altar stood. The site of the
temple, then, is so far established for an almost universal belief is
entertained in the authenticity of the tradition regarding the rock El
Sakhara; and it has also been conclusively proved that the area of the
temple was identical on its western, eastern, and southern sides with
the present enclosure of the haram [ROBINSON].
"That the temple was situated somewhere within the oblong
enclosure on Mount Moriah, all topographers are agreed, although there
is not the slightest vestige of the sacred fane now remaining; and the
greatest diversity of sentiment prevails as to its exact position
within that large area, whether in the center of the haram, or in its
southwest corner" [BARCLAY]. Moreover, the full
extent of the temple area is a problem that remains to be solved, for
the platform of Mount Moriah being too narrow for the extensive
buildings and courts attached to the sacred edifice, Solomon resorted
to artificial means of enlarging and levelling it, by erecting vaults,
which, as JOSEPHUS states, rested on immense
earthen mounds raised from the slope of the hill. It should be borne in
mind at the outset that the grandeur of the temple did not consist in
its colossal structure so much as in its internal splendor, and the
vast courts and buildings attached to it. It was not intended for the
reception of a worshipping assembly, for the people always stood in the
outer courts of the sanctuary.

