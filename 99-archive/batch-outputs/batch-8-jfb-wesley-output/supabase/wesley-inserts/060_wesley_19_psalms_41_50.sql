execute judgment upon us. Cease - Or delay to sit in judgment.
Tempestuous - This is a farther description of that terrible majesty,
wherewith God would clothe himself when he came to his tribunal.

4. Call - To the inhabitants of them, all angels and men,
whom he calls in for witnesses of the equity of his proceedings.

5. Gather - O ye angels, summon and fetch them to my
tribunal. Which is poetically spoken, to continue the metaphor, and
representation of the judgment. My saints - The Israelites, whom God had
chosen and separated them from all the nations of the earth, to be an
holy and peculiar people to himself, and they also had solemnly devoted
themselves to God; all which aggravated their apostacy. Those - Who have
entered into covenant with me, and have ratified that covenant by
sacrifice. This seems to be added, to acquaint them with the proper
nature, use and end of sacrifices, which were principally appointed to
be signs and seals of the covenant made between God and his people; and
consequently to convince them of their great mistake in trusting to
their outward sacrifices, when they neglected the very life and soul of
them, which was the keeping of their covenant with God.

6. Declare - God will convince the people of his
righteousness, and of their own wickedness, by thunders and lightnings,
and storms, or other dreadful signs wrought by him in the heavens.
Himself - In his own person. God will not now reprove them, by his
priests or prophets, but in an extraordinary manner from heaven.

7. Hear - Having brought in God, as coming to judgment,
he now gives an account of the process and sentence of the judge.
Testify - I will declare my charge against thee. Thy God - Not only in
general, but in a special manner, by that solemn covenant made at Sinai;
whereby I avouched thee to be my peculiar people, and thou didst avouch
me to be thy God.

8. I will not - This is not the principal matter of my
charge, that thou hast neglected sacrifices which thou shouldst have
offered.

