and where, and how much, and how long each shall blow. He only doth all
these things, and he only knows why he doth them. He instanceth in some
few of God''s works, and those which seem to be most trivial, and
uncertain, that thereby he might more strongly imply that God doth the
same in other things which are more considerable, that he doth all
things in the most exact order, and weight, and measure. The waters -
Namely, the rain-waters, which God layeth up in his store-houses, the
clouds, and thence draws them forth, and sends them down upon the earth
in such times and proportions as he thinks fit. Measure - For liquid
things are examined by measure, as other things are by weight: and here
is both weight and measure to signify with what perfect wisdom God
governs the world.

26. When - At the first creation, when he settled that
course and order which should be continued. A decree - An appointment
and as it were a statute law, that it should fall upon the earth, in
such times, and places, and proportions.

27. It - Wisdom, which is the subject of the present
