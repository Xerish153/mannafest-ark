Commentary on Chapter XXVIII

1. Surely - Job having in the last chapter discoursed of
God''s various providences toward wicked men, and shewed that God doth
sometimes, for a season, give them prosperity, but afterwards calls them
to a sad account, and having shewed that God doth sometimes prosper the
wicked all their days, so they live and die without any visible token of
God''s displeasure, when on the contrary, good men are exercised with
many calamities; and perceiving that his friends were, scandalized at
these methods of Divine providence, and denied the thing, because they
could not understand the reason of such dispensations: in this chapter
he declares that this is one of the depths of Divine wisdom, not
discoverable by any mortal man, and that although men had some degree of
wisdom whereby they could search out many hidden things, as the veins of
silver, and gold, yet this was a wisdom of an higher nature, and out of
man''s reach. The caverns of the earth he may discover, but not the
counsels of heaven.

3. Perfection - Whatever is deeply wrought in the deepest
caverns. Stones of darkness - The precious stones which lie hid in the
dark bowels of the earth, where no living thing can dwell.

4. Breaketh out - While men are searching, water breaks
in upon them. Inhabitants - Out of that part of the earth which the
miners inhabit. Forgotten - Untrodden by the foot of man. Dried up -
They are dried up, (or, drawn up, by engines made for that purpose) from
men, from the miners, that they may not be hindered in their work.

5. Fire - Coals, and brimstone, and other materials of
fire. Unless this refer, as some suppose, to a central fire.

6. Sapphires - Of precious stones; the sapphire, is one
of the most eminent, being put for all the rest. In some parts of the
earth, the sapphires are mixed with stones, and cut out of them and
polished. Hath - The earth continueth. Dust - Distinct from that gold
which is found in the mass, both sorts of gold being found in the
earth.

7. A path - In the bowels of the earth. Vulture''s eye -
Whose eye is very quick, and strong, and searcheth all places for its
prey.

8. Lion - Which rangeth all places for prey. The birds
and beasts have often led men to such places as otherwise they should
never have found out; but they could not lead them to these mines, the
finding out of them is a special gift of God.

9. He, &c. - This and the two next verses mention
other eminent works of God, who overturneth rocks, and produceth new
rivers.

10. Seeth - Even those which no human art or industry was
ever able to discover. 12. That wisdom - Man hath one kind of wisdom, to
discover the works of nature, and to perform the operations of art; but
as for that sublime wisdom which consists in the knowledge of God and
ourselves, no man can discover this, but by the special gift of God.

13. Found - Among men upon earth, but only among those
blessed spirits that dwell above.

14. The depth - This is not to be found in any part of
the sea, though a man may dig or dive ever so deep to find it, nor to be
learned from any creature.

20. Whence, &c. - By a diligent inquiry, we find at
length, that there is a twofold wisdom; one hid in God, which belongs
not to us, the other revealed to man, which belongs to us and to our
children.

21. Hid - The line and plummet of human reason, can never
fathom the abyss of the Divine counsels. Who can account for the maxims,
measures and methods of God''s government? Let us then be content, not to
know the future events of providence, ''till time discover them: and not
to know the secret reasons of providence, ''till eternity brings them to
light.

22. Death - The grave, the place of the dead, to ''which
these things are here ascribed, as they are to the depths, and to the
sea, by a common figure. Though they cannot give an account of it
themselves yet there is a world, on which these dark regions border,
where we shall see it clearly. Have patience, says death: I will fetch
thee shortly to a place where even this wisdom shall be found. When the
veil of flesh is rent, and the interposing clouds are scattered, we
shall know what God doth, though we know not now.

23. God - God alone. The way - The methods which he takes
in the management of all affairs, together with its grounds and ends in
them. The place - Where it dwells, which is only in his own mind.

24. For - He, and he only knows it, because his
