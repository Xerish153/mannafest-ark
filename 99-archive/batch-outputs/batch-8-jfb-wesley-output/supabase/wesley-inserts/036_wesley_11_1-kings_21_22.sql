host of heaven; if you consider, first, that their original seat was in
heaven. Secondly, that the name of heaven is often given to all that
part of the world which is above the earth, and among the rest, to the
air, and where the devil''s residence and dominion lies, Eph. ii, 2, and that both Michael and his angels, and the Dragon and
his angels, are said to be, and to wage war in heaven, Rev. xii, 7, either the air, or the church.

